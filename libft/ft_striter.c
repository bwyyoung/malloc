#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	char	*sp;

	if (s != NULL && (*f) != NULL)
	{
		sp = s;
		while (*sp != '\0')
		{
			(*f)(sp);
			sp++;
		}
	}
}
