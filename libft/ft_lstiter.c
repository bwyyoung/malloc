#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list		*list_ptr;

	if (lst == NULL)
		return ;
	list_ptr = lst;
	while (list_ptr->next != NULL)
	{
		(*f)(list_ptr);
		list_ptr = list_ptr->next;
	}
	(*f)(list_ptr);
}
